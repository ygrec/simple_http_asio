/*
 * Расчет контрольной суммы (полином x^8 + x^5 + x^4 + x^0)
 */
uint8_t CalcCRC8_31_slow(uint8_t *data, uint32_t count, uint8_t InitValue)
{
	unsigned char crc = 0xFF - InitValue;
	unsigned char i;
	//	unsigned char len = 4;

	while (count--)
	{
		crc ^= *data++;

		for(i = 0; i < 8; i++)
			crc = crc & 0x80 ? (crc << 1) ^ 0x31 : crc << 1;
	}
	return crc;
}

/****************************************************************************
 *			Расчет контрольной суммы CRC16 с начальным значением			*
 ****************************************************************************/
uint16_t CalcCRC16(uint8_t *data, uint32_t count, uint16_t InitValue)
{
	uint16_t CRC = 0xFFFF - InitValue;
	while (count--)
	{
		uint_fast8_t index = *data++;
		index ^= CRC >> 8;
		CRC <<= 8;
		CRC ^= CRC16Table[index];
	}
	return (0xFFFF - CRC);
}



/*
 * Расчет контрольной суммы (полином a^8 + a^5 + a^4 + 1) для датчиков уровня топлива OMNICOMM LLS20x
 */
uint8_t CalcCRC8_LLS20xxx(const uint8_t crcInit, const uint8_t * data, const uint8_t len)
{
	uint8_t ret = crcInit;
	uint8_t count;

	for(count = 0; count < len; count++)
	{
		uint8_t b = data[count], i = 8;
		do
		{
			if((b ^ ret) & 0x01)
				ret = ((ret ^ 0x18) >> 1) | 0x80;
			else
				ret >>= 1;
			b >>= 1;
		}while(--i);
	}
	return ret;
}

/*
 * Расчет контрольной суммы CRC32 ISO
 * полином  (0xEDB88320) (x^26 + x^23 + x^22 + x^16 + x^12 + x^11 + x^10 + x^8 + x^7 + x^5 + x^4 + x^2 + x^1 + 1)
 */
uint32_t crc32_update(uint32_t crc, uint8_t a)
{
	int i;
	crc ^= a;
	for(i = 0; i < 8; ++i)
	{
		if(crc & 1)
			crc = (crc >> 1) ^ 0xEDB88320;
		else
			crc = (crc >> 1);
	}
	return crc;
}


uint16_t crc16_update(uint16_t crc, uint8_t a)
{
	int i;
	crc ^= a;
	for(i = 0; i < 8; ++i)
	{
		if(crc & 1)
			crc = (crc >> 1) ^ 0xA001;
		else
			crc = (crc >> 1);
	}
	return crc;
}


uint16_t crc16_calc(const uint16_t crcInitValue, const uint8_t *data, const uint16_t dataSize)
{
	uint16_t crc = crcInitValue;
	uint16_t count;
	for(count = 0; count < dataSize; count++)
	{
		crc = crc16_update(crc, *(data + count));
	}

	return crc;
}







