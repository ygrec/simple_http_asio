/****************************************************************************
 * @name Универсальный расчет CRC
 * @brief http://www.zorc.breitbandkatze.de/crc.html
 *
 * @param Набор параметров для расчета CRC кадра обмена модема в режиме MUX
 ***************************************************************************/
const int order = 8;
const unsigned long polynom = 0x07;
const int direct = 1;
const unsigned long crcinit = 0xff;
const unsigned long crcxor = 0xff;
const int refin = 1;
const int refout = 1;

unsigned long crcmask = 0xFF;
unsigned long crchighbit = 0x01 << (8 - 1);

// subroutines

unsigned long reflect (unsigned long crc, int bitnum) {

	// reflects the lower 'bitnum' bits of 'crc'

	unsigned long i, j=1, crcout=0;

	for(i=(unsigned long)1<<(bitnum-1); i; i>>=1) {
		if(crc & i) crcout|=j;
		j<<= 1;
	}
	return (crcout);
}




unsigned long crcbitbybitfast(unsigned char* p, unsigned long len) {

	// fast bit by bit algorithm without augmented zero bytes.
	// does not use lookup table, suited for polynom orders between 1...32.

	unsigned long i, j, c, bit;
	unsigned long crc = crcinit;

	for(i=0; i<len; i++) {

		c = (unsigned long)*p++;
		if(refin) c = reflect(c, 8);

		for(j=0x80; j; j>>=1) {

			bit = crc & crchighbit;
			crc<<= 1;
			if(c & j) bit^= crchighbit;
			if(bit) crc^= polynom;
		}
	}

	if(refout) crc=reflect(crc, order);
	crc^= crcxor;
	crc&= crcmask;

	return(crc);
}


/****************************************************************************
 * @name CompressOldRecordBuffer G
 * @brief Сжатие матрицы байт по алгоритму RLE.
 * Проход по столбцам данных. Переход от столбца к столбцу не сбрасывает сжатие.
 *
 * @param
 * @return none
 ***************************************************************************/
size_t CompressRleMatrix(size_t ColumnQuantity, size_t RowQuantity, uint8_t *InBuffer, uint8_t *OutBuffer)
{
	size_t columnCount, rowCount;
	uint8_t repeatCount;
	uint8_t repeatSymbol;
	volatile uint8_t *currentInput;
	uint8_t *currentOutByte = OutBuffer;


	// Перебор всех столбцов записи
	for(columnCount = 0; columnCount < ColumnQuantity; columnCount++)
	{
		// Первый элемент очередного столбца
		currentInput = InBuffer + columnCount;

		repeatCount = 0;
		repeatSymbol = *currentInput; //Чтобы 1ое значение всегда выводилось
		*(currentOutByte++) = repeatSymbol;

		currentInput += ColumnQuantity;

		// Перебор всех записей набора
		for(rowCount = 1; rowCount < RowQuantity; rowCount++)
		{
			if(repeatSymbol == *currentInput)
			{
				repeatCount++;
			}
			else
			{
				if(repeatCount > 0)
				{
					repeatCount--;
					*(currentOutByte++) = repeatSymbol;
					*(currentOutByte++) = repeatCount;
					repeatCount = 0;
				}
				repeatSymbol = *currentInput;
				*(currentOutByte++) = *currentInput;
			}


			// Смещение к элементу следующей строки
			currentInput += ColumnQuantity;
		}

		if(repeatCount > 0)
		{
			repeatCount--;
			*(currentOutByte++) = repeatSymbol;
			*(currentOutByte++) = repeatCount;
		}
	}

	return (currentOutByte - OutBuffer);
}


/****************************************************************************
 * @name
 * @brief Транспонирование матрицы
 *
 * @param inputData - входная матрица
 * @return none
 ***************************************************************************/
void Transposition(void * inputData, void * outputData, size_t ColumnNumber, size_t RowNumber)
{
	uint8_t *inputMatrix, *outputMatrix;
	uint16_t columnCount;
	for(columnCount = 0; columnCount < ColumnNumber; columnCount++)
	{
		uint16_t rowCount;
		for(rowCount = 0; rowCount < RowNumber; rowCount++)
		{
			inputMatrix = (uint8_t *)inputData + ColumnNumber * rowCount + columnCount;
			outputMatrix = (uint8_t *)outputData + RowNumber * columnCount + rowCount;
			*outputMatrix = *inputMatrix;
		}
	}

}


/****************************************************************************
 * @name
 * @brief Сжатие RLE массива
 *
 * @param none
 * @return none
 ***************************************************************************/
size_t CompressRleArray(const void *inputArray, void * outputArray, size_t dataSize)
{
	uint8_t *currentInput = (uint8_t *)inputArray;

	size_t columnCount;
	int8_t repeateCount;
	uint8_t repeateSymbol;
	uint8_t *currentOutput = (uint8_t *) outputArray;
	uint16_t compressedDataSize = 0;

	repeateCount = 0;
	repeateSymbol = *currentInput++; //Чтобы 1ое значение всегда выводилось
	compressedDataSize = 1;
	uint8_t *repeateCounterPtr = (uint8_t *)currentOutput++;

	// Перебор всех полей массива
	for(columnCount = 1; columnCount < dataSize; columnCount++)
	{
		if(repeateSymbol == *currentInput)
		{
			if(repeateCount == 0)
			{
				*currentOutput++ = repeateSymbol;
				repeateCount++;
				compressedDataSize++;
			}
			else
			{
				if(0 < repeateCount && repeateCount < 126)
				{
					repeateCount++;
				}
				else
				{
					if(repeateCount == 126)
					{
						*repeateCounterPtr = repeateCount + 1;
						repeateCounterPtr = currentOutput++;
						repeateSymbol = *currentInput;
						repeateCount = 0;
						compressedDataSize += 1;
					}
					else
					{
						*repeateCounterPtr = repeateCount;
						repeateCounterPtr = currentOutput - 1;
						*currentOutput++ = repeateSymbol;
						repeateCount = 1;
						compressedDataSize++;
					}
				}
			}
		}
		else
		{
			{
				if(-125 < repeateCount && repeateCount <= 0)
				{
					if(repeateCount == 0)
					{
						*currentOutput++ = repeateSymbol;
						compressedDataSize++;
					}
					repeateCount--;
					repeateSymbol = *currentInput;
					*currentOutput++ = repeateSymbol;
					compressedDataSize++;
				}
				else
				{
					if(repeateCount == -125)
						*repeateCounterPtr = repeateCount - 1;
					else
						*repeateCounterPtr = repeateCount + 1;

					repeateCounterPtr = currentOutput++;
					repeateSymbol = *currentInput;
					repeateCount = 0;
					compressedDataSize++;
				}
			}
		}
		// Смещение к следующему элементу
		currentInput++;
	}

	if(repeateCount > 0)
	{
		repeateCount++;
		*repeateCounterPtr = repeateCount;
	}
	else
	{
		if(repeateCount == 0)
		{
			*repeateCounterPtr = -1;
			*currentOutput++ = repeateSymbol;
			compressedDataSize++;
		}
		else
		{
			*repeateCounterPtr = repeateCount - 1;
			*currentOutput = repeateSymbol;
		}
	}

	return compressedDataSize;
}


// https://en.wikipedia.org/wiki/Luhn_algorithm
// used for verify IMEI
uint8_t LunhControlSum(uint8_t *Array, size_t ArraySize)
{
	uint8_t sum = 0;
	size_t i;
	for(i = 0; i < ArraySize; i++)
	{
	   uint8_t p = Array[i];
	   if((i % 2) != 0)
	   {
	     p = 2*p;
	     if (p > 9)
	       p = p - 9;
		}
	   sum = sum + p;
	}
	 //дополнение до 10
	 sum = 10 - (sum % 10);
	 if (sum == 10)
	   sum = 0;

	 return sum;
}

