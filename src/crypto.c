/**
 * CRC-8
 * Poly : 0x91 x^8 + x^7 + x^4 + 1
 * Init : 0xFF
 * Revert: false
 * XorOut: 0xFF
 * Check :
 */
uint8_t CalcCRC8_91(uint8_t *data, uint32_t count, uint8_t InitValue)
{
	uint8_t CRC = 0xFF - InitValue;
	while (count)
	{
		CRC = CRC8Table[CRC ^ *data++];
		count--;
	}
	return (0xFF - CRC);
}


